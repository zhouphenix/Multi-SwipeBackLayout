    @Override
    public boolean onSwipeBackPre(@SwipeBackLayout.DragDirection int direction, float touchX, float touchY) {
        if (direction == SwipeBackLayout.LEFT && !drawer.isDrawerOpen(GravityCompat.START)) {
            drawer.openDrawer(GravityCompat.START);
            return true;
        }
        return  super.onSwipeBackPre(direction, touchX, touchY);
    }

    @Override
    public void onBackPressed() {
        if (drawer.isDrawerOpen(GravityCompat.START)) {
            drawer.closeDrawer(GravityCompat.START);
        } else {
            super.onBackPressed();
        }
    }

